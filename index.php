    <h2 class="featurette-heading">Nutrición <span class="text-muted"></span></h2>
    <p class="lead">El desarrollo nutricional que los niños y jóvenes deben tener durante su proceso de crecimiento tanto cognitivo como físico, se basa en tener una alimentación balanceada que favorezca el correcto funcionamiento del cuerpo, de lo contrario, una alimentación poco balanceada no favorece el correcto desarrollo de la persona. Después de conocer este proceso, se diversifica el concepto, es decir no desde una perspectiva general, sino una perspectiva de acuerdo a la necesidad de la persona, en este caso una alimentación de acuerdo a su estatura, peso y metabolismo.</p>
